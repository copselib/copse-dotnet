using Microsoft.CodeAnalysis;
using Microsoft.CodeAnalysis.CSharp;
using Microsoft.CodeAnalysis.CSharp.Syntax;
using System;
using System.Collections.Generic;
using System.IO;
using System.Linq;

// Prototype async->sync source generator (the Npgsql pattern): transcribe a direct-style async
// treenumerator into its synchronous twin by a mechanical, semantics-free rewrite. Not production
// grade -- just enough to prove the transcription is faithful (the generated sync driver passes the
// same conformance battery). Usage: Copse.CodeGen <input.cs> <output.g.cs>

if (args.Length != 2)
{
  Console.Error.WriteLine("usage: Copse.CodeGen <input.cs> <output.g.cs>");
  return 1;
}

var input = args[0];
var output = args[1];

var tree = CSharpSyntaxTree.ParseText(File.ReadAllText(input));
var root = (CompilationUnitSyntax)tree.GetRoot();

var rewritten = (CompilationUnitSyntax)new AsyncToSyncRewriter().Visit(root);

// Two type-name fixups that aren't identifier renames (they change to keywords): return types.
var text = rewritten.ToFullString()
  .Replace("ValueTask<bool>", "bool")
  .Replace("ValueTask", "void");

var header =
  "// <auto-generated>\n" +
  "//   Generated from " + Path.GetFileName(input) + " by Copse.CodeGen (async->sync transcription).\n" +
  "//   Do not edit; edit the async source and regenerate.\n" +
  "// </auto-generated>\n";

File.WriteAllText(output, header + text);
Console.WriteLine($"generated {output}");
return 0;

// Mechanical async->sync rewrite: strip async/await/ConfigureAwait, drop async-only usings, rename the
// async identifiers to their sync counterparts, and rehome the namespace. Trivia is preserved so the
// output stays readable and close to the source.
internal sealed class AsyncToSyncRewriter : CSharpSyntaxRewriter
{
  // Identifier renames (declarations and uses alike). Ordered longest-first is unnecessary -- these are
  // whole-token matches, not substrings.
  private static readonly Dictionary<string, string> Renames = new()
  {
    ["AsyncDepthFirstTreenumerator"] = "GeneratedDepthFirstTreenumerator",
    ["AsyncWhereDepthFirstTreenumerator"] = "GeneratedWhereDepthFirstTreenumerator",
    ["IAsyncTreenumerator"] = "ITreenumerator",
    ["IAsyncChildEnumerator"] = "IForwardChildEnumerator",
    ["IAsyncEnumerable"] = "IEnumerable",
    ["IAsyncEnumerator"] = "IEnumerator",
    ["TAsyncChildEnumerator"] = "TChildEnumerator",
    ["GetAsyncEnumerator"] = "GetEnumerator",
    ["MoveNextAsync"] = "MoveNext",
    ["OnMoveNextAsync"] = "OnMoveNext",
    ["MoveToNextRootNodeAsync"] = "MoveToNextRootNode",
    ["OnSchedulingAsync"] = "OnScheduling",
    ["OnVisitingAsync"] = "OnVisiting",
    ["BacktrackAsync"] = "Backtrack",
    ["TryPushNextChildAsync"] = "TryPushNextChild",
    ["DisposeAsync"] = "Dispose",
  };

  // Usings that only make sense in the async source.
  private static readonly HashSet<string> DroppedUsings = new()
  {
    "Copse.Core.Async",
    "System.Threading.Tasks",
  };

  public override SyntaxNode VisitUsingDirective(UsingDirectiveSyntax node)
    => DroppedUsings.Contains(node.Name?.ToString()) ? null : base.VisitUsingDirective(node);

  public override SyntaxNode VisitNamespaceDeclaration(NamespaceDeclarationSyntax node)
  {
    node = (NamespaceDeclarationSyntax)base.VisitNamespaceDeclaration(node);
    // Rehome the async namespace to a sibling ".Generated" (Copse.Async -> Copse.Generated,
    // Copse.Linq.Async -> Copse.Linq.Generated).
    var ns = node.Name.ToString();
    if (ns.EndsWith(".Async"))
    {
      var generated = ns.Substring(0, ns.Length - ".Async".Length) + ".Generated";
      node = node.WithName(SyntaxFactory.ParseName(generated).WithTriviaFrom(node.Name));
    }
    return node;
  }

  // Strip `await X` -> `X`.
  public override SyntaxNode VisitAwaitExpression(AwaitExpressionSyntax node)
    => ((ExpressionSyntax)Visit(node.Expression)).WithTriviaFrom(node);

  // Strip `X.ConfigureAwait(false)` -> `X`.
  public override SyntaxNode VisitInvocationExpression(InvocationExpressionSyntax node)
  {
    if (node.Expression is MemberAccessExpressionSyntax ma && ma.Name.Identifier.ValueText == "ConfigureAwait")
      return ((ExpressionSyntax)Visit(ma.Expression)).WithTriviaFrom(node);
    return base.VisitInvocationExpression(node);
  }

  // Drop the `async` modifier.
  public override SyntaxNode VisitMethodDeclaration(MethodDeclarationSyntax node)
  {
    node = (MethodDeclarationSyntax)base.VisitMethodDeclaration(node);
    if (node.Modifiers.Any(SyntaxKind.AsyncKeyword))
    {
      var async = node.Modifiers.First(m => m.IsKind(SyntaxKind.AsyncKeyword));
      // Preserve the async token's leading trivia on whatever follows so indentation is kept.
      var mods = SyntaxFactory.TokenList(node.Modifiers.Where(m => !m.IsKind(SyntaxKind.AsyncKeyword)));
      node = node.WithModifiers(mods);
    }
    return node;
  }

  // Rename identifier tokens, preserving trivia.
  public override SyntaxToken VisitToken(SyntaxToken token)
  {
    token = base.VisitToken(token);
    if (token.IsKind(SyntaxKind.IdentifierToken) && Renames.TryGetValue(token.ValueText, out var renamed))
      return SyntaxFactory.Identifier(token.LeadingTrivia, renamed, token.TrailingTrivia);
    return token;
  }
}
