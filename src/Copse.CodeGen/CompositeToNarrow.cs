using Microsoft.CodeAnalysis;
using Microsoft.CodeAnalysis.CSharp;
using Microsoft.CodeAnalysis.CSharp.Syntax;
using System.Collections.Generic;

namespace Copse.CodeGen
{
  /// <summary>
  /// The composite-&gt;narrow transcription: mechanically rewrite a composite-width (both-dimension)
  /// SelectWhere-lattice type into its single-dimension twin. Public + pure (string in, string out)
  /// so both the regen tool and the drift-guard test drive the exact same transform.
  ///
  /// <para>The dimension axis, like the color axis, cannot be abstracted by C# generics (the
  /// successor type constructor is what varies), so the narrow twins are generated instead of
  /// hand-mirrored: the source interface and every return type narrow to the entry's dimension,
  /// the lattice types rename to their narrow twins, and the other dimension's acquisition method
  /// is dropped. The output is itself an async source: the async-&gt;sync manifest picks the
  /// generated twins up as inputs, so one composite-width async file fans out to five generated
  /// ones (narrow async x2, then sync x3).</para>
  /// </summary>
  public static class CompositeToNarrow
  {
    public static string Transform(NarrowGeneratorEntry entry, string source)
    {
      var root = (CompilationUnitSyntax)CSharpSyntaxTree.ParseText(source).GetRoot();
      var rewritten = (CompilationUnitSyntax)new Rewriter(entry.Dimension).Visit(root);

      var header =
        "// <auto-generated>\n" +
        "//   Generated from " + System.IO.Path.GetFileName(entry.WideSource) + " by Copse.CodeGen (composite->narrow transcription).\n" +
        "//   Do not edit; edit the composite-width source and regenerate: dotnet run --project Copse.CodeGen\n" +
        "// </auto-generated>\n";

      // Emit CRLF regardless of the source's line endings (.editorconfig: end_of_line = crlf).
      return (header + rewritten.ToFullString()).Replace("\r\n", "\n").Replace("\n", "\r\n");
    }

    private sealed class Rewriter : CSharpSyntaxRewriter
    {
      // The lattice renames, exact-match per identifier token: the core contract narrows and every
      // SelectWhere-lattice type maps to its same-width twin in the entry's dimension. Deliberately
      // a closed list (never a suffix rule): only lattice types participate, and the drivers'
      // near-miss names (AsyncSelectTreenumerator, AsyncSelectPruneAfterTreenumerator) must pass
      // through untouched -- both getters already construct the right driver, the kept one is
      // correct as-is. A new wrapper family joining the lattice adds its pair here and its rows to
      // the manifest.
      private static readonly Dictionary<string, string> LatticeTypes = new()
      {
        ["IAsyncTreenumerable"] = "IAsync{0}Treenumerable",
        ["IAsyncSelectWhereTreenumerable"] = "IAsyncSelectWhere{0}Treenumerable",
        ["IAsyncSelectPruneAfterTreenumerable"] = "IAsyncSelectPruneAfter{0}Treenumerable",
        ["SelectWhereTreenumerable"] = "SelectWhere{0}Treenumerable",
        ["AsyncSelectTreenumerable"] = "AsyncSelect{0}Treenumerable",
        ["AsyncPruneAfterTreenumerable"] = "AsyncPruneAfter{0}Treenumerable",
        ["AsyncSelectPruneAfterTreenumerable"] = "AsyncSelectPruneAfter{0}Treenumerable",
      };

      private readonly Dictionary<string, string> _renames;
      private readonly string _droppedAcquisition;

      public Rewriter(NarrowDimension dimension)
      {
        var kept = dimension == NarrowDimension.DepthFirst ? "DepthFirst" : "BreadthFirst";

        _renames = new Dictionary<string, string>();
        foreach (var pair in LatticeTypes)
          _renames[pair.Key] = string.Format(pair.Value, kept);

        _droppedAcquisition = dimension == NarrowDimension.DepthFirst
          ? "GetAsyncBreadthFirstTreenumerator"
          : "GetAsyncDepthFirstTreenumerator";
      }

      // Drop the other dimension's acquisition method (declaration in the wrapper sources; the
      // interfaces declare none -- the narrow core contract already carries the kept one).
      public override SyntaxNode VisitMethodDeclaration(MethodDeclarationSyntax node)
      {
        if (node.Identifier.ValueText == _droppedAcquisition)
          return null;

        return base.VisitMethodDeclaration(node);
      }

      // Rename identifier tokens, preserving trivia.
      public override SyntaxToken VisitToken(SyntaxToken token)
      {
        token = base.VisitToken(token);
        if (!token.IsKind(SyntaxKind.IdentifierToken))
          return token;

        if (_renames.TryGetValue(token.ValueText, out var renamed))
          return SyntaxFactory.Identifier(token.LeadingTrivia, renamed, token.TrailingTrivia);

        return token;
      }
    }
  }
}
